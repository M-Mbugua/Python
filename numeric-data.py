#Program to identify numeric and boolean data types in python

print("Python has three numeric types; int, float and complex")


#Integer 
myvalue=1

print(myvalue)
print(type(myvalue))
print(str(myvalue) + " is of the data type " + str(type(myvalue)))


#Float 
myvalue=3.14

print(myvalue)
print(type(myvalue))
print(str(myvalue) + " is of the data type " + str(type(myvalue)))


#Complex 
myvalue=5j
print(myvalue)
print(type(myvalue))
print(str(myvalue) + " is of the data type " + str(type(myvalue)))


#Boolean 
myvalue=True
print(myvalue)
print(type(myvalue))
print(str(myvalue) + " is of the data type " + str(type(myvalue)))
